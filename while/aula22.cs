using System;

class Aula21
{
    static void Main(){

        //LOOP WHILE
        int i=0;
        while (i<2)
        {
            Console.WriteLine(i);
            i++;
        }
        Console.WriteLine("fim do loop");

        //LOOP DO WHILE
        do 
        {
            Console.WriteLine(i);
            i++;
        }while(i<1);

    }
}

